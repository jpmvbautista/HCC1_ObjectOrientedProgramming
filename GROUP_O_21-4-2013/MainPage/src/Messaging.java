    private void SendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendActionPerformed
        // TODO add your handling code here:
         nLbl = NameTF.getText();
         SLbl = StudentTF.getText();
         Mlbl = MessageTF.getText();
         
         IntClass m = new IntClass();
         
         m.setName(nLbl);
         m.setStudentNumber(SLbl);
         m.setMessage(Mlbl);
         
           File outFile;
        FileWriter fw;
        BufferedWriter bw;
        
        String to = NameTF.getText();
        String from = StudentTF.getText();
        String msg = MessageTF.getText();
                
        try{
            outFile = new File("Message.txt");
            fw = new FileWriter(outFile, true);
            bw = new BufferedWriter(fw);
            
            bw.write(to);
            bw.newLine();
            
            bw.write(from);
            bw.newLine();
              
            bw.write(msg);
            bw.newLine();
            bw.close();
        
        } catch(IOException e){
            System.out.println("Error writing to file: " + e);
        }
         
         
         
    }//GEN-LAST:event_SendActionPerformed

