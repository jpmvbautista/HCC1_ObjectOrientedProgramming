    private void FileBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FileBtnActionPerformed
        // TODO add your handling code here:  String to;
        String nLbl;
        String SLbl;
        String Mlbl;
        File inFile;
        FileReader fr;
        BufferedReader br;
        
        try{ 
            inFile = new File("Message.txt");
            fr = new FileReader(inFile);
            br = new BufferedReader(fr);
            
            nLbl = br.readLine();
            SLbl = br.readLine();
            Mlbl = br.readLine();
            
            while((nLbl != null) || (SLbl != null) || (Mlbl != null)){
                JOptionPane.showMessageDialog(null, "To: "+ nLbl +"\n From: "+ SLbl +"\n Message: "+ Mlbl);
                nLbl = br.readLine();               
                SLbl = br.readLine();
                Mlbl = br.readLine();
            }
            br.close();
            
        }catch (IOException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_FileBtnActionPerformed

